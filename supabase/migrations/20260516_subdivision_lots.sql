-- Subdivision Lots: table + Miguel Marques seed
-- Supports loteamentos (subdivisions) with per-lot availability tracking

CREATE TABLE IF NOT EXISTS public.subdivision_lots (
  id UUID PRIMARY KEY DEFAULT gen_random_uuid(),
  development_id UUID NOT NULL REFERENCES public.developments(id) ON DELETE CASCADE,
  quadra VARCHAR(5) NOT NULL,
  lot_number INTEGER NOT NULL,
  area_m2 DECIMAL(10,2) NOT NULL,
  price DECIMAL(12,2),
  status VARCHAR(20) NOT NULL DEFAULT 'DISPONIVEL',
  special_type VARCHAR(30),
  notes TEXT,
  created_at TIMESTAMPTZ DEFAULT NOW(),
  updated_at TIMESTAMPTZ DEFAULT NOW(),
  UNIQUE (development_id, quadra, lot_number)
);

CREATE INDEX IF NOT EXISTS idx_subdivision_lots_development ON public.subdivision_lots(development_id);
CREATE INDEX IF NOT EXISTS idx_subdivision_lots_status ON public.subdivision_lots(status);
CREATE INDEX IF NOT EXISTS idx_subdivision_lots_quadra ON public.subdivision_lots(development_id, quadra);

ALTER TABLE public.subdivision_lots ENABLE ROW LEVEL SECURITY;

CREATE POLICY "subdivision_lots_public_read" ON public.subdivision_lots
  FOR SELECT USING (true);

CREATE POLICY "subdivision_lots_auth_write" ON public.subdivision_lots
  FOR ALL USING (auth.uid() IS NOT NULL);

